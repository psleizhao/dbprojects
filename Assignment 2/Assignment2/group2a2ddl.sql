--  DDL for View PLAYLISTS_VIEW
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "GROUP2USER"."PLAYLISTS_VIEW" ("PLAYLISTID", "PLAYLISTNAME", "OWNERID", "ISPUBLIC", "ISCOLLABORATIVE") AS 
  select playlists.PlaylistID as PlaylistID, playlistname.playlistname as playlistname,
        playlists.ownerID as ownerID, playlists.ispublic as ispublic, playlists.iscollaborative
    from playlists
    join playlistname_playlist on playlistname_playlist.playlistid = playlists.playlistid
    join playlistname on playlistname.playlistnameid = playlistname_playlist.playlistnameid
    where playlistname_playlist.endtime is null
;
--------------------------------------------------------
--  DDL for View SONGS_VIEW
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "GROUP2USER"."SONGS_VIEW" ("SPOTIFYID", "TRACKNAME", "ALBUMNAME", "RELEASEDATE", "DURATION", "POPULARITY", "DANCEABILITY") AS 
  select songs.spotifyID as spotifyID,  trackname, albumname, releasedate, duration, popularity, danceability
    from songs
    join songpop on songpop.songid = songs.spotifyID
    join popularities on popularities.popID = songpop.popID
    where songpop.endtime is null
;
